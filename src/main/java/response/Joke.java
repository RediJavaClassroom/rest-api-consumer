package response;

public class Joke {
    private String type;
    private String setup;
    private String punchline;
    private int id;

    public String getType() {
        return type;
    }

    public String getSetup() {
        return setup;
    }

    public String getPunchline() {
        return punchline;
    }

    public int getId() {
        return id;
    }
}
